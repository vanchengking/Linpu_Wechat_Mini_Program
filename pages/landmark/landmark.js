  // 打开详情弹窗
  openDetail(e) {
    const index = e.currentTarget.dataset.index;
    const building = this.data.buildings[index];
    this.setData({
      currentBuilding: building,
      showModal: true
    });
    
    // 添加页面滚动锁定
    wx.hideTabBar();
  },

  // 关闭详情弹窗
  closeDetail() {
    this.setData({
      showModal: false,
      currentBuilding: null
    });
    
    // 解除页面滚动锁定
    wx.showTabBar();
  },

  // 页面加载完成
